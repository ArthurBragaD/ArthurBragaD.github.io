                        <p style="margin-top:0cm;">
                            Foi no dia 3 de maio de 1929, que as cortinas do Cine Theatro Avenida, atual
                            Teatro Municipal do Rio Grande, se abriram pela primeira vez. Localizado no
                            chamado Boulevard Major Carlos Pinto – hoje rua Major Carlos Pinto – que na
                            época separava a “cidade antiga da cidade nova”. O Avenida seguiu com suas
                            atividades até o ano de 1983, quando foi encampado pelo Executivo Municipal.
                            Assim, a partir do seu restauro, aconteceu a abertura para a comunidade como
                            Teatro Municipal do Rio Grande.
                            <br><br>
                            O Teatro Municipal do Rio Grande possui o Memorial Cine Theatro Avenida, inaugurado em 18 de maio de 2001, integrando o Programa de Educação Patrimonial do município com o objetivo de reunir o acervo que conta um pouco da história desta casa cultural. O acervo fica localizado em uma sala específica e nele encontramos o Projetor de Filmes – BAUER, usado na reprodução dos filmes e também o cartaz do primeiro filme que inaugurou o Cine Theatro. Também podemos ver fotos e cartazes dos espetáculos e de artistas que por aqui passaram, além de livros sobre a dramaturgia nacional e internacional.
                            <br><br>Nestes 92 anos, o Cine Theatro Avenida permanece com o seu nome na fachada do prédio, mas ainda carrega a marca do fomento à cultura, que perpassou décadas de história ligadas ao universo artístico, hoje sinônimo do trabalho deste espaço de cultura do Rio Grande.
                            <br><br>O Teatro Municipal pelo Decreto Nº13.961 é um bem que compõe o patrimônio histórico que faz parte dos Bens de Interesse Sócio Cultural do Município, registrado no Inventário do Patrimônio Cultural do Rio Grande do Sul – IPHAN.
