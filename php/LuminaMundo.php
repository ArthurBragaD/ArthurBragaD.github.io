                        <p style="margin-top:0cm;">
                            O projeto Lumina Mundo: leitura itinerante consiste em oferecer à comunidade rio-grandina o acesso e fomento ao livro, especialmente no que diz respeito à ampliação da leitura literária. Dessa forma, se utiliza de um equipamento de cultura itinerante, furgão, que funciona como uma biblioteca ambulante. Sendo assim, ao estacionar em lugares estratégicos de fruição de pessoas, como: praias, feiras, espaços de lazer e outros eventos, disponibiliza para a comunidade um acervo de livros que poderão ser adquiridos de forma gratuita. Também, se propõe a doação de livros pela comunidade para o projeto, assim possibilitando uma circulação orgânica de livros dentro da cidade.
                            <br><br>
                            Diante de um mundo voltado para as tecnologias, o livro físico passa a ocupar, muitas vezes, um papel secundário no cenário ao qual estamos inseridos. Sem desprezar as plataformas digitais e as redes sociais, que crescem em acelerado ritmo, os diferentes públicos leitores rendem-se a essa não tão nova modalidade de acesso à informação. Sendo assim, o projeto Lumina mundo atua como um importante equipamento de cultura, capaz de transformar os impedimentos relacionados com a não fruição da leitura em motivos para colocar a mesma na rotina da comunidade.

                            <br><br>Desde novembro de 2021 até agosto de 2022, o projeto é desenvolvido no município percorrendo diferentes lugares da cidade. Assim, com atividades quinzenais e outras eventuais, o projeto já distribuiu aproximadamente 1.500 livros para a comunidade da cidade de Rio Grande, ou que moram no município ou que estão de passagem. Também, a ação conta com diferentes pontos de coleta espalhados pela cidade, sendo um deles o próprio furgão, a Biblioteca Rio-grandese e a Secretária de Cultura, Esporte e Lazer do Município.
