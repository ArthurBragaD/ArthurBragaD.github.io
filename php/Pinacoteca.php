                        <p style="margin-top:0cm;">A
                            Pinacoteca Municipal Matteo Tonietti é uma instituição museológica, fundada em
                            02 de setembro de 1985 pelo decreto nº 4.609. Teve como gênese a Escola de Belas
                            Artes Heitor de Lemos a partir de obras de artes que a Escola apresentava e que
                            foi adquirida junto ao corpo discente e docente da referida Escola. Esta coleção
                            de obras no ano de 1985 repassada para o prédio do Centro Municipal de Cultura
                            Inah Emil Martensen que já tinha sido criado e inaugurado no mês de março. Assim
                            no dia 02 de setembro de 1985 sob o decreto nº 4.609 a instituição foi criada e
                            a partir desta data, deu-se início ao trabalho de documentação e aquisição de
                            bens para o município do Rio Grande cuja finalidade era enriquecer o acervo e
                            então da instituição recém fundada.
                            <br><br>
                            As peças deste acervo mostram diferentes formas de expressões, materiais diversificados, recursos gráficos e outros elementos que integram a elaboração artística e em cujas obras podem-se ver contextualizados momentos políticos, ideológicos, econômicos, artísticos e culturais ou que registrem particularidades da Nação Brasileira ou de outras civilizações e países representativos da nossa humanidade.
                            <br><br>No inventário da instituição podemos ver obras de Francisco Stokinger, Vasco Prado, Danúbio Gonçalves, Paulo Porcella, Brito Velho, Esther Benetti Bianco, Hilda Mattos, Nelson Jungbluht, Geraldo Roberto da Silva, Michael Chapman, Marcelo Calheiros, Leopoldo Gotuzzo, Alice Soares, Marcelo Calheiros, Guilherme Miller, Biba Mattos, Lenir Miranda, Claudio Maciel, Walmir Souza, Ricardo Giovannini e Matteo Tonietti, Walmir Souza, Elizabeth Costa, Carmem Medeiros, Clóvis Garcia, Armando Almeida, Mara Caruso, Carmem Jardim, Inês Benetti, Carlos Medeiros, Cláudio Cardoso Felgas, Ieda Papaleo, entre outros artistas que enriquecem o acervo tanto com seu currículo como com sua produção artística.
                            <br><br>No ano 2009 o Prefeito Municipal Fábio de Oliveira Branco através do Decreto 10.288 de 27 de maio de 2009 conferiu a denominação da instituição a qual passou a chamar-se Pinacoteca Municipal Matteo Tonietti. O nome de Matteo Tonietti é uma homenagem ao grande pintor, escultor e fotógrafo que nasceu em 02 de março de 1882, em Nizza i Savóia, cidade de fronteira norte franco-italiana. Matteo Tonietti aportou em Rio Grande aos nove anos de idade e aos poucos foi dominando o novo idioma da nova terra. O garoto quando ainda morava em Nizza já esculpia bonecos na areia e quando teve que lutar para vencer as dificuldades financeiras trabalhou como sapateiro com o Sr. Rocco Rolante onde durante o trabalho se concentrava e desenhava o retrato do patrão, como também o de todos que ali chegavam.
                            <br><br> A família notando a grande percepção artística do menino permitiu que ele começasse a ter aulas com Ricardo Giovaninni, grande mestre e fotógrafo na região da época. Mais tarde, Matteo Tonietti volta a Buenos Aires para aprimorar os ensinamentos recebidos com o grande professor. O artista deixou um acervo representativo na cidade do Rio Grande entre pinturas e esculturas as quais podemos vê-las tanto na Praça Tamandaré como na Praça Xavier Ferreira bem como no Museu da Cidade do Rio Grande, na Pinacoteca Municipal Matteo Tonietti e em muitas outras instituições e residências de famílias da cidade e também em outras localidades do estado.
                            <br><br> A Pinacoteca Municipal Matteo Tonietti está aguardando um local para exercer todas as atividades propostas com seus respectivos espaços pela Legislação de Museus.
